class QuizModel {
  final String id;
  final String correctAnswer;
  final List<String> incorrectAnswers;
  final String question;
  QuizModel({
    required this.id,
    required this.correctAnswer,
    required this.incorrectAnswers,
    required this.question,
  });

  List<String> options() {
    var options = [correctAnswer, ...incorrectAnswers];
    // options.shuffle();
    return options;
  }

  factory QuizModel.fromJson(var json) {
    return QuizModel(
      id: json["id"] ?? "",
      correctAnswer: json["correctAnswer"] ?? "",
      incorrectAnswers: List.from(json["incorrectAnswers"] ?? [])
          .map((e) => e.toString())
          .toList(),
      question: json["question"]["text"] ?? "",
    );
  }
}
